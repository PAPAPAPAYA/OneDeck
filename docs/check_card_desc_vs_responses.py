def normalize_punctuation(text):
    # Replace common English punctuation used in descriptions with Chinese equivalents
    # for consistent parsing, but keep colons as markers.
    text = text.replace("；", ";")
    text = text.replace("，", ",")
    text = text.replace("：", ":")
    return text


def split_segments(desc):
    desc = normalize_punctuation(strip_tags(desc))
    # Split by ; or newline
    segs = re.split(r"[;\n]", desc)
    return [s.strip() for s in segs if s.strip()]


def extract_trigger_event(segment):
    """Infer GameEvent name from the trigger phrase of a segment."""
    if ":" in segment:
        trig = segment.split(":", 1)[0].strip()
    else:
        trig = ""
        # No explicit trigger phrase; infer from utility shop effects
        if re.search(r"卡位增加|生命值上限增加", segment):
            return "OnMeBought"
        return "OnMeRevealed"  # default

    patterns = [
        ("当敌方[诅咒]揭晓", "OnHostileCurseRevealed"),
        ("当敌方[诅咒]获得力量", "OnEnemyCurseCardGotPower"),
        ("当友方被埋葬", "OnFriendlyCardBuried"),
        ("友方被埋葬", "OnFriendlyCardBuried"),
        ("当卡被埋葬", "OnFriendlyCardBuried"),
        ("当友方被去除", "OnFriendlyCardExiled"),
        ("友方被去除", "OnFriendlyCardExiled"),
        ("当敌人受到伤害", "OnTheirPlayerTookDmg"),
        ("敌人受到伤害", "OnTheirPlayerTookDmg"),
        ("当友方获得力量", "OnFriendlyCardGotPower"),
        ("敌人被揭晓", "OnHostileRevealed"),
        ("洗牌后", "AfterShuffle"),
        ("被置顶", "OnMeStaged"),
        ("被埋葬", "OnMeBuried"),
        ("获得力量时", "OnMeGotStatusEffect"),
        ("揭晓", "OnMeRevealed"),
        ("每", "OnMeRevealed"),
    ]
    for kw, ev in patterns:
        if kw in trig:
            return ev
    return "OnMeRevealed"


def extract_expected_categories(segment):
    """Return list of expected semantic effect categories from an effect phrase."""
    cats = []
    eff = segment.split(":", 1)[1] if ":" in segment else segment
    eff = eff.strip()
    if not eff:
        return cats

    # Split effect phrase into clauses by comma/semicolon so cross-clause
    # keywords (e.g. "埋葬 ... , 增强 ... 敌方") are not mixed up.
    clauses = re.split(r"[,;]", eff)

    for clause in clauses:
        clause = clause.strip()
        if not clause:
            continue

        # Damage
        if re.search(r"造成\s*\d*\s*伤害|造成\s*\[力量\].*?伤害|伤害\s*x", clause):
            if re.search(r"对自己|自身", clause):
                cats.append("DAMAGE_SELF")
            else:
                cats.append("DAMAGE_OPPONENT")
        elif "对自己" in clause and "伤害" in clause:
            cats.append("DAMAGE_SELF")

        # Bury
        if re.search(r"埋葬\s*\d*\s*友方|埋葬所有友方", clause):
            cats.append("BURY_FRIENDLY")
        if re.search(r"埋葬\s*\d*\s*敌方", clause):
            cats.append("BURY_HOSTILE")
        if re.search(r"埋葬后\s*\d+\s*卡", clause):
            cats.append("BURY_NEXT")

        # Stage
        if re.search(r"置顶\s*\d*\s*友方|置顶自身", clause):
            cats.append("STAGE_FRIENDLY")
        if re.search(r"置顶\s*\d*\s*敌方|置顶力量最多的敌方", clause):
            cats.append("STAGE_HOSTILE")

        # Curse enhance
        if re.search(r"增强\s*(?:\d+\s*)?敌方\s*\[诅咒\](?:\s*\d+)?|增强敌方\[诅咒\]", clause):
            cats.append("ENHANCE_CURSE")
        if re.search(r"增强\s*(?:\d+\s*)?自身诅咒", clause):
            cats.append("ENHANCE_FRIENDLY_CURSE")
        if re.search(r"\[诅咒\].*?获得\s*\d*\s*力量", clause):
            cats.append("ENHANCE_CURSE")

        # Consume curse power
        if re.search(r"消耗敌方\[诅咒\].*?力量|敌方\[诅咒\]力量\s*-\s*\d+", clause):
            cats.append("CONSUME_CURSE_POWER")

        # Give status effect
        if re.search(r"给予\s*\d*\s*友方\s*\d*\s*力量|给予下\s*\d+\s*卡\s*\d*\s*力量|所有友方获得\s*\d*\s*力量|给予该友方", clause):
            cats.append("GIVE_STATUS_EFFECT")
        if re.search(r"获得\s*\d*\s*力量|获得\s*\d*\s*倍力量|翻倍.*?力量", clause):
            # "[诅咒]获得力量" is handled as curse enhance above
            if not re.search(r"\[诅咒\].*?获得\s*\d*\s*力量", clause):
                cats.append("GIVE_STATUS_EFFECT")

        # Consume status effect
        if re.search(r"去除\s*\d*\s*敌方\s*\d*\s*力量|消耗\s*\d+\s*力量", clause):
            cats.append("CONSUME_STATUS_EFFECT")

        # Transfer status effect
        if "转移" in clause and "力量" in clause:
            cats.append("TRANSFER_STATUS_EFFECT")

        # Add temp card / copy curse
        if re.search(r"生成\s*\d*\s*\[次元裂缝\]", clause):
            cats.append("ADD_CARD_FRIENDLY")
        if re.search(r"复制敌方\s*\d*\s*\[诅咒\]", clause):
            cats.append("COPY_CURSE")
        if "添加自身到卡组" in clause:
            cats.append("ADD_CARD_FRIENDLY")

        # Exile
        if re.search(r"放逐\s*\d*\s*友方", clause):
            cats.append("EXILE_FRIENDLY")
        if re.search(r"放逐\s*\d*\s*敌方", clause):
            cats.append("EXILE_ENEMY")
        if "去除自身" in clause or "放逐自身" in clause:
            cats.append("EXILE_SELF")

        # Utility
        if re.search(r"卡位增加", clause):
            cats.append("DECK_SIZE_UP")
        if re.search(r"生命值上限增加", clause):
            cats.append("HP_MAX_UP_SELF")

    return list(set(cats))



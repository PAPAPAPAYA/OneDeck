# -*- coding: utf-8 -*-
"""
Check whether card descriptions match the actual GameEventListener responses
(event -> CostNEffectContainer -> effect method bindings) in 3.0 no cost cards.

Reads: docs/CardDesc_Response_Check.txt (generated by Unity execute_code)
Outputs: docs/CardDesc_Response_Mismatch_Report.md
"""
import re
import os

PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
INPUT_PATH = os.path.join(PROJECT_ROOT, "docs", "CardDesc_Response_Check.txt")
OUTPUT_PATH = os.path.join(PROJECT_ROOT, "docs", "CardDesc_Response_Mismatch_Report.md")


def parse_log(path):
    cards = []
    with open(path, "r", encoding="utf-8-sig") as f:
        for line in f:
            line = line.strip()
            if not line.startswith("CARD|"):
                continue
            parts = line.split("|", 4)
            if len(parts) < 5:
                continue
            name = parts[1]
            asset_path = parts[2]
            full = parts[3] + "|" + parts[4]
            if "|bindings=" not in full:
                continue
            desc_part, after = full.split("|bindings=", 1)
            if not desc_part.startswith("cardDesc="):
                continue
            desc = desc_part[len("cardDesc="):].replace("\\n", "\n").replace("\\r", "\r").replace("\\|", "|")

            listeners = []
            # Match each listener binding block; container names may contain ']' so we anchor at the trailing ' costs=...]'.
            for m in re.finditer(r"\[LISTENER\s+(.+?)\s+costs=(.*?)\]", after):
                inner = m.group(1).strip()
                costs_text = m.group(2).strip()
                listener = {"event": "NONE", "container": "", "effects": [], "costs": parse_calls(costs_text)}
                # Parse key=value pairs. Values may contain spaces; split at spaces preceding known keys.
                tokens = re.split(r"\s+(?=(?:event|container|effects|costs)=)", inner)
                for token in tokens:
                    if "=" not in token:
                        continue
                    k, v = token.split("=", 1)
                    k = k.strip()
                    v = v.strip()
                    if k == "event":
                        listener["event"] = v
                    elif k == "container":
                        listener["container"] = v
                    elif k == "effects":
                        listener["effects"] = parse_calls(v)
                listeners.append(listener)
            cards.append({"name": name, "path": asset_path, "desc": desc, "listeners": listeners})
    return cards


def parse_calls(text):
    calls = []
    if not text:
        return calls
    # Match calls like: Full.Type.Name[, Assembly]->MethodName(0)
    # The type may contain a comma-prefixed assembly name; method is a simple identifier.
    pattern = re.compile(
        r"([A-Za-z_][A-Za-z0-9_.]*(?:,\s*[A-Za-z_][A-Za-z0-9_.-]*)*)\s*->\s*([A-Za-z_][A-Za-z0-9_]*)\s*\((-?\d+)\)"
    )
    for m in pattern.finditer(text):
        full_type = m.group(1).strip()
        # Strip assembly suffix and namespace, keep only class name
        type_name = full_type.split(",")[0].strip()
        type_name = type_name.split(".")[-1].strip()
        method = m.group(2).strip()
        arg = int(m.group(3))
        calls.append({"type": type_name, "method": method, "arg": arg})
    return calls


def strip_tags(text):
    return re.sub(r"</?[^>]+>", "", text)



# Matching
# -----------------------------------------------------------------------------

COMPATIBLE_CATEGORIES = {
    "DAMAGE_OPPONENT": {"DAMAGE_OPPONENT"},
    "DAMAGE_SELF": {"DAMAGE_SELF"},
    "BURY_FRIENDLY": {"BURY_FRIENDLY", "BURY_TAG"},
    "BURY_HOSTILE": {"BURY_HOSTILE", "BURY_TAG"},
    "BURY_NEXT": {"BURY_NEXT"},
    "BURY_SELF": {"BURY_SELF"},
    "STAGE_FRIENDLY": {"STAGE_FRIENDLY", "STAGE_MOST_STATUS"},
    "STAGE_HOSTILE": {"STAGE_HOSTILE", "STAGE_MOST_STATUS"},
    "ENHANCE_CURSE": {"ENHANCE_CURSE"},
    "ENHANCE_FRIENDLY_CURSE": {"ENHANCE_FRIENDLY_CURSE"},
    "CONSUME_CURSE_POWER": {"CONSUME_CURSE_POWER"},
    "GIVE_STATUS_EFFECT": {"GIVE_STATUS_EFFECT"},
    "CONSUME_STATUS_EFFECT": {"CONSUME_STATUS_EFFECT"},
    "TRANSFER_STATUS_EFFECT": {"TRANSFER_STATUS_EFFECT"},
    "ADD_CARD_FRIENDLY": {"ADD_CARD_FRIENDLY"},
    "ADD_CARD_ENEMY": {"ADD_CARD_ENEMY"},
    "COPY_CURSE": {"COPY_CURSE"},
    "EXILE_FRIENDLY": {"EXILE_FRIENDLY", "EXILE_TAG"},
    "EXILE_ENEMY": {"EXILE_ENEMY", "EXILE_TAG"},
    "EXILE_SELF": {"EXILE_SELF"},
    "EXILE_RANDOM": {"EXILE_RANDOM"},
    "DECK_SIZE_UP": {"DECK_SIZE_UP"},
    "HP_MAX_UP_SELF": {"HP_MAX_UP_SELF"},
}


def categories_match(expected_cat, actual_cats):
    """Check whether any actual category satisfies the expected category."""
    allowed = COMPATIBLE_CATEGORIES.get(expected_cat, {expected_cat})
    return bool(set(actual_cats).intersection(allowed))


def check_card(card):
    mismatches = []
    desc = card["desc"]
    segments = split_segments(desc)

    for seg in segments:
        trigger = extract_trigger_event(seg)
        expected_cats = extract_expected_categories(seg)
        if not expected_cats:
            # No recognizable effect in this segment (e.g. pure trigger like "萦绕")
            continue

        # Find listener bindings for this trigger event
        matching_listeners = [lst for lst in card["listeners"] if lst["event"] == trigger]
        if not matching_listeners:
            mismatches.append({
                "segment": seg,
                "trigger": trigger,
                "expected": expected_cats,
                "issue": "缺少对应触发事件的 Listener",
                "actual_listeners": [],
            })
            continue

        # Collect all actual effect categories from those listeners
        actual_cats = []
        for lst in matching_listeners:
            for call in lst["effects"]:
                actual_cats.extend(method_categories(call))
        actual_cats = list(set(actual_cats))

        # Check each expected category
        for exp_cat in expected_cats:
            if not categories_match(exp_cat, actual_cats):
                mismatches.append({
                    "segment": seg,
                    "trigger": trigger,
                    "expected": [exp_cat],
                    "issue": "效果类型不匹配",
                    "actual_listeners": matching_listeners,
                })
                break  # one mismatch per segment is enough

    # Also detect orphan listeners: event not described by any segment
    described_triggers = set()
    for seg in segments:
        described_triggers.add(extract_trigger_event(seg))

    orphan_listeners = []
    for lst in card["listeners"]:
        if lst["event"] not in described_triggers:
            orphan_listeners.append(lst)

    return mismatches, orphan_listeners


def listener_summary(lst):
    lines = ["- `" + lst["container"] + "` (" + lst["event"] + ")"]
    for call in lst["effects"]:
        lines.append("  - " + call["type"] + "->" + call["method"] + "(" + str(call["arg"]) + ")")
    return "\n".join(lines)


def build_report(cards):
    lines = []
    lines.append("# 卡片描述与 GameEventListener Response 对应检查报告")
    lines.append("")
    lines.append("**检查范围**：`Assets/Prefabs/Cards/3.0 no cost (current)` 下全部卡片")
    lines.append("")
    lines.append("**检查逻辑**：")
    lines.append("1. 将 `cardDesc` 拆分为独立触发段落。")
    lines.append("2. 根据段落推断期望的触发事件与效果语义类别（如 `造成 X 伤害` → 伤害、`置顶友方` → 置顶友方等）。")
    lines.append("3. 读取每个 `GameEventListener` 的触发事件及其 `Response` 实际调用的 `CostNEffectContainer`，并提取 `effectEvent` 方法。")
    lines.append("4. 若某段描述的效果在对应事件的 Listener/Container 中找不到匹配的方法，则标记为问题。")
    lines.append("")

    issue_cards = []
    for card in cards:
        mismatches, orphans = check_card(card)
        if mismatches or orphans:
            issue_cards.append((card, mismatches, orphans))

    total = len(cards)
    ok = total - len(issue_cards)
    lines.append("## 摘要")
    lines.append("")
    lines.append("| 项目 | 数量 |")
    lines.append("|---|---|")
    lines.append("| 检查卡片总数 | " + str(total) + " |")
    lines.append("| 无问题 | " + str(ok) + " |")
    lines.append("| 存在疑似不匹配 | " + str(len(issue_cards)) + " |")
    lines.append("")

    if not issue_cards:
        lines.append("## 结果")
        lines.append("")
        lines.append("所有卡片的描述与 Listener Response 均匹配，未发现明显不匹配。")
        lines.append("")
        return "\n".join(lines)

    lines.append("## 疑似不匹配卡片")
    lines.append("")

    for idx, (card, mismatches, orphans) in enumerate(issue_cards, 1):
        lines.append("### " + str(idx) + ". " + card["name"])
        lines.append("")
        lines.append("**路径**：`" + card["path"] + "`")
        lines.append("")
        lines.append("**描述**：")
        lines.append("```")
        lines.append(card["desc"])
        lines.append("```")
        lines.append("")

        if mismatches:
            lines.append("**描述段落与 Response 不匹配**：")
            lines.append("")
            lines.append("| 描述段落 | 期望事件 | 期望效果 | 问题 | 实际 Container |")
            lines.append("|---|---|---|---|---|")
            for m in mismatches:
                actual = ""
                if m["actual_listeners"]:
                    actual = "<br>".join([lst["container"] + " (" + lst["event"] + "): " +
                                          ", ".join([call["type"] + "->" + call["method"] for call in lst["effects"]])
                                          for lst in m["actual_listeners"]])
                else:
                    actual = "无"
                lines.append("| " + m["segment"] + " | " + m["trigger"] + " | " + ", ".join(m["expected"]) + " | " + m["issue"] + " | " + actual + " |")
            lines.append("")

        if orphans:
            lines.append("**未在描述中体现的 Listener**：")
            lines.append("")
            for lst in orphans:
                lines.append(listener_summary(lst))
            lines.append("")

    lines.append("---")
    lines.append("")
    lines.append("**注意**：本报告基于关键词与方法名的语义匹配，复杂表述、多效果组合或特殊逻辑可能需要人工复核。")
    lines.append("")
    return "\n".join(lines)


def main():
    cards = parse_log(INPUT_PATH)
    report = build_report(cards)
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        f.write(report)
    print("Report written to:", OUTPUT_PATH)
    print("Cards checked:", len(cards))


if __name__ == "__main__":
    main()

def method_categories(call):
    """Return list of semantic categories for a C# method call."""
    t = call["type"]
    m = call["method"]
    cats = []

    if t == "HPAlterEffect":
        if m.startswith("DecreaseTheirHp"):
            cats.append("DAMAGE_OPPONENT")
        elif m.startswith("DecreaseMyHp"):
            cats.append("DAMAGE_SELF")
        elif m.startswith("IncreaseTheirHp"):
            cats.append("HEAL_OPPONENT")
        elif m.startswith("IncreaseMyHp"):
            cats.append("HEAL_SELF")
    elif t == "BuryEffect":
        if m in ("BuryMyCards", "BuryAllMyCards", "BuryMyCardsWithTag", "BuryMyCards_BasedOnIntSO"):
            cats.append("BURY_FRIENDLY")
        elif m in ("BuryTheirCards", "BuryTheirCardsWithTag", "BuryTheirCards_BasedOnIntSO"):
            cats.append("BURY_HOSTILE")
        elif m == "BuryNextXCards":
            cats.append("BURY_NEXT")
        elif m == "BuryCardsWithTag":
            cats.append("BURY_TAG")
        elif m == "BurySelf":
            cats.append("BURY_SELF")
    elif t == "StageEffect":
        if m in ("StageSelf", "StageMyCards", "StageMyCards_BasedOnIntSO", "StageMyCardsWithTag",
                 "StageMyTokens", "StageAllFriendlyMinion"):
            cats.append("STAGE_FRIENDLY")
        elif m in ("StageTheirCardsWithTag", "StageTheirSpecificCard"):
            cats.append("STAGE_HOSTILE")
        elif m == "StageCardWithMostStatusEffect":
            cats.append("STAGE_MOST_STATUS")
    elif t == "CurseEffect":
        if m in ("EnhanceCurse", "EnhanceCurse_BasedOnIntSO", "EnhanceCurseWithCoefficient_BasedOnIntSO"):
            cats.append("ENHANCE_CURSE")
        elif m == "EnhanceFriendlyCurse":
            cats.append("ENHANCE_FRIENDLY_CURSE")
        elif m == "ConsumeHostileCursePower":
            cats.append("CONSUME_CURSE_POWER")
    elif t == "StatusEffectGiverEffect":
        if m.startswith("Give"):
            cats.append("GIVE_STATUS_EFFECT")
    elif t == "PowerReactionEffect":
        if m.startswith("Give"):
            cats.append("GIVE_STATUS_EFFECT")
    elif t == "StatusEffectAmplifierEffect":
        if m.startswith("Amplify"):
            cats.append("GIVE_STATUS_EFFECT")
    elif t == "ConsumeStatusEffect":
        if m.startswith("Consume"):
            cats.append("CONSUME_STATUS_EFFECT")
    elif t == "TransferStatusEffectEffect":
        if m.startswith("Transfer"):
            cats.append("TRANSFER_STATUS_EFFECT")
    elif t == "AddTempCard":
        if m in ("AddCardToMe", "AddSelfToMe"):
            cats.append("ADD_CARD_FRIENDLY")
        elif m in ("AddCardToThem", "AddSelfToThem"):
            cats.append("ADD_CARD_ENEMY")
        elif m == "CopyEnemyCurseCardToThem":
            cats.append("COPY_CURSE")
    elif t == "ExileEffect":
        if m == "ExileSelf":
            cats.append("EXILE_SELF")
        elif m in ("ExileMyCards", "ExileMyCardsWithTag", "ExileMyMinions", "ExileMyCardsWithTypeID",
                   "ExileMyCards_BasedOnIntSO"):
            cats.append("EXILE_FRIENDLY")
        elif m in ("ExileTheirCards", "ExileTheirCardsWithTag", "ExileTheirMinions",
                   "ExileTheirCards_BasedOnIntSO"):
            cats.append("EXILE_ENEMY")
        elif m == "ExileRandomCards":
            cats.append("EXILE_RANDOM")
        elif m == "ExileCardsWithTag":
            cats.append("EXILE_TAG")
    elif t == "DeckSizeIncreaseEffect":
        cats.append("DECK_SIZE_UP")
    elif t == "HPMaxAlterEffect":
        if m.startswith("IncreaseMyHpMax"):
            cats.append("HP_MAX_UP_SELF")
        elif m.startswith("IncreaseTheirHpMax"):
            cats.append("HP_MAX_UP_OPPONENT")

    return cats



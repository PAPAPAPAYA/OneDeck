	for (int pi = 0; pi < paths.Length; pi++)
	{
		string path = paths[pi];
		var prefab = UnityEditor.AssetDatabase.LoadAssetAtPath<UnityEngine.GameObject>(path);
		if (prefab == null)
		{
			UnityEngine.Debug.LogError("NOT_FOUND: " + path);
			continue;
		}
		var cardScript = prefab.GetComponent<CardScript>();
		if (cardScript == null)
		{
			UnityEngine.Debug.LogError("NO_CARDSCRIPT: " + path);
			continue;
		}

		CardInfo info = new CardInfo();
		info.prefabName = prefab.name;
		info.assetPath = path;
		info.cardTypeID = cardScript.cardTypeID != null ? cardScript.cardTypeID : "";
		info.displayName = cardScript.displayName != null ? cardScript.displayName : "";
		if (info.displayName == "") info.displayName = prefab.name;
		info.cardDesc = cardScript.cardDesc != null ? cardScript.cardDesc : "";
		info.rarity = cardScript.rarity.ToString();

		System.Text.StringBuilder statusBuilder = new System.Text.StringBuilder();
		if (cardScript.myStatusEffects != null)
		{
			for (int i = 0; i < cardScript.myStatusEffects.Count; i++)
			{
				if (i > 0) statusBuilder.Append(", ");
				statusBuilder.Append(cardScript.myStatusEffects[i].ToString());
			}
		}
		info.myStatusEffects = statusBuilder.ToString();

		library.cards.Add(info);
	}


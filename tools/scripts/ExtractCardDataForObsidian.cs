	string projectRoot = System.IO.Path.GetDirectoryName(UnityEngine.Application.dataPath);
	string outPath = System.IO.Path.Combine(projectRoot, "tools", "outputs", "OneDeckCardLibrary.json");
	string outDir = System.IO.Path.GetDirectoryName(outPath);
	if (!System.IO.Directory.Exists(outDir))
	{
		System.IO.Directory.CreateDirectory(outDir);
	}
	System.IO.File.WriteAllText(outPath, json);
	UnityEngine.Debug.Log("Wrote card library JSON to: " + outPath + " (count=" + library.cards.Count + ")");
	return 0;
